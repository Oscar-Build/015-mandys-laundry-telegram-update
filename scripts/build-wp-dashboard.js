'use strict';

/**
 * Creates or updates the SEO Dashboard page on the WordPress site.
 * Run once to publish it; thereafter the GitHub Actions workflow keeps
 * data.json fresh and the embedded iframe reflects the latest numbers.
 *
 * Usage:  node scripts/build-wp-dashboard.js
 */

require('dotenv').config();

const axios = require('axios');

const WP_API  = process.env.WORDPRESS_API_URL;
const WP_USER = process.env.WORDPRESS_USERNAME;
const WP_PASS = process.env.WORDPRESS_APP_PASSWORD;
const AUTH    = 'Basic ' + Buffer.from(`${WP_USER}:${WP_PASS}`).toString('base64');
const DASH_URL = 'https://oscar-build.github.io/015-mandys-laundry-telegram-update/';
const PAGE_SLUG = 'seo-dashboard';

if (!WP_API || !WP_USER || !WP_PASS) {
  console.error('ERROR: WP credentials not set in .env');
  process.exit(1);
}

const PAGE_CONTENT = `
<!-- SEO Dashboard — auto-generated by Mandy's Laundry Automation -->
<style>
  .mls-dash-wrap { font-family: -apple-system, sans-serif; max-width: 100%; }
  .mls-dash-frame { width: 100%; height: 90vh; min-height: 800px; border: none; border-radius: 12px; box-shadow: 0 2px 12px rgba(0,0,0,.12); }
  .mls-dash-note { font-size: 12px; color: #94a3b8; margin-top: 8px; text-align: right; }
  .mls-dash-link { color: #2563eb; text-decoration: none; }
  .mls-dash-link:hover { text-decoration: underline; }
</style>

<div class="mls-dash-wrap">
  <iframe
    class="mls-dash-frame"
    src="${DASH_URL}"
    title="Mandy's Laundry SEO Dashboard"
    loading="lazy"
  ></iframe>
  <p class="mls-dash-note">
    Data updates daily at 6:10 AM PST &nbsp;·&nbsp;
    <a class="mls-dash-link" href="${DASH_URL}" target="_blank" rel="noopener">Open full dashboard ↗</a>
  </p>
</div>
`.trim();

async function findExistingPage() {
  try {
    const res = await axios.get(`${WP_API}/pages`, {
      params: { slug: PAGE_SLUG, per_page: 1 },
      headers: { Authorization: AUTH },
      timeout: 15000,
    });
    return res.data?.[0] || null;
  } catch { return null; }
}

async function run() {
  console.log('Building WordPress SEO Dashboard page...');

  const existing = await findExistingPage();

  const payload = {
    title:   "SEO Dashboard",
    slug:    PAGE_SLUG,
    content: PAGE_CONTENT,
    status:  'publish',
    meta:    { _yoast_wpseo_metadesc: "Live SEO dashboard for Mandy's Laundry — publishing, indexing, traffic and Search Console data." },
  };

  let result;
  if (existing) {
    console.log(`Updating existing page (ID: ${existing.id})...`);
    const res = await axios.patch(`${WP_API}/pages/${existing.id}`, payload, {
      headers: { Authorization: AUTH, 'Content-Type': 'application/json' },
      timeout: 20000,
    });
    result = res.data;
    console.log('✅ Page updated:', result.link);
  } else {
    console.log('Creating new page...');
    const res = await axios.post(`${WP_API}/pages`, payload, {
      headers: { Authorization: AUTH, 'Content-Type': 'application/json' },
      timeout: 20000,
    });
    result = res.data;
    console.log('✅ Page created:', result.link);
  }

  console.log('\nDashboard URL on WordPress:', result.link);
  console.log('Add this URL to your Telegram end-of-day report if preferred over GitHub Pages.');
}

run().catch(err => {
  console.error('Failed:', err.response?.data?.message || err.message);
  process.exit(1);
});
